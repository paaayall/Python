class Employee:
    def __init__(self, empName, empSalary, empCity):
        self.empName =   empName
        self .empSalary = empSalary
        self .empCity = empCity
    def getEmpName(self):
        return self.empName
    def getEmpSalary(self):
        return self.empSalary
    def getEmpCity(self):
        return self.empCity
